# Nhập hai số nguyên từ người dùng
num1 = int(input("Nhập số thứ nhất: "))
num2 = int(input("Nhập số thứ hai: "))

# Tính tổng
tong = num1 + num2

# Hiển thị kết quả
print(f"Tổng của {num1} và {num2} là: {tong}")

# Tính hiệu
hieu = num1 - num2

# Hiển thị kết quả hiệu 2 số
print(f"Hiệu của {num1} và {num2} là: {hieu}")

